#ifndef Node_hpp
#define Node_hpp

#include <stdio.h>
#include <string>
#include <iostream>

template<typename T>
struct Node
{
    //node that points to the next object
    //node that points to the front of the list
    T* object;
    Node<T>* next = nullptr;
    Node(T* data);
    ~Node();
};

template<typename T>
Node<T>::Node(T* data)
{
    object = data;
    next = nullptr;
}
//destructor
template<typename T>
Node<T>::~Node()
{
    delete object;
    if(next != nullptr)
        delete next;
    
}

#endif /* Node_hpp */
